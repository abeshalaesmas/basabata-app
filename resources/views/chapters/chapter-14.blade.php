        Lubhâng karapatdapat dalawin ang bahay na tinitirahán ni Makaraig.Malakí, maluwang, may <span class=hover-this>dalawáng<span class=hover-text>Doble o magkapares.</span></span> na entresuelo na may magagaràng saláng-bakal, warì’y isáng páaralan sa mg̃a unang oras ng̃ umaga at isáng hinalòng linugaw magmulâ namán sa ika sampû. Sa mg̃a oras ng̃ paglilibáng ng̃ mg̃a nang̃ung̃upahan, mulâ sa pagpasok sa maluwang na silong hanggán sa itáas ng̃ kabahayán, ay walâng humpáy ang tawanan, ang kaguluhan at ang galawan. May mg̃a binatàng damít pangbahay na naglalarô ng̃ sipà, nang̃agsasanay sa pagpapalakás sa pamagitan ng̃ mg̃a trapesiong gawâ lamang nilá; sa hagdanan ay nagpapamook ang waló ó siyam na ang mg̃a sandata ay mg̃a tungkód, mg̃a sibát, mg̃a <span class=hover-this>kalawit<span class=hover-text>[pangngalan] Isang uri ng panghuli o panghuli ng isda.</span></span> at silò, ng̃unì’t ang mg̃a lumulusob at nilulusob ay karaniwan nang hindî nagkakasakitan; ang mg̃a palò ay tumatamà sa likód ng̃ insík na nagbibilì ng̃ kaniyáng tindáng mg̃a kakanín at nakasisiràng kalamay, sa tabí ng̃ hagdán. Nalilibid siyá ng̃ maraming batà, binabatak ang kaniyáng buhók na kalág na’t gusót, inaagawan siyá ng̃ isáng kalamay, tinatawaran sa halagá, at ginagawán ng̃ ibá’t ibáng <span class=hover-this>kabuktután.<span class=hover-text>[pangngalan] Kalokohan o kasamaan.</span></span> Ang insík ay nagsísisigaw, nagtutung̃ayáw sa lahát ng̃ wikàng kaniyáng batíd, sampû sa kaniyáng sariling wikà, mag-iiyakiyakan, tatawa, sasamò, isásayá ang múkhâ kung walâng mangyari sa kaniyá sa masamâ, ó pabalík.


